package com.rescam.xhb.framework.entity;
/**
 * 商品目录
 * @author xueyang
 *
 */
public class GoodsCatalog {
	
	private Integer goodsCatalogId;//目录id
	private Integer parentCatalogId;//父目录id
	private String goodsCatalogName;//目录名称
	private String goodsCatalogStatus;//状态0：已删除 1：未删除
	
	public Integer getGoodsCatalogId() {
		return goodsCatalogId;
	}
	public void setGoodsCatalogId(Integer goodsCatalogId) {
		this.goodsCatalogId = goodsCatalogId;
	}
	public Integer getParentCatalogId() {
		return parentCatalogId;
	}
	public void setParentCatalogId(Integer parentCatalogId) {
		this.parentCatalogId = parentCatalogId;
	}
	public String getGoodsCatalogName() {
		return goodsCatalogName;
	}
	public void setGoodsCatalogName(String goodsCatalogName) {
		this.goodsCatalogName = goodsCatalogName;
	}
	public String getGoodsCatalogStatus() {
		return goodsCatalogStatus;
	}
	public void setGoodsCatalogStatus(String goodsCatalogStatus) {
		this.goodsCatalogStatus = goodsCatalogStatus;
	}
	public GoodsCatalog(Integer goodsCatalogId, Integer parentCatalogId,
				String goodsCatalogName, String goodsCatalogStatus) {
		super();
		this.goodsCatalogId = goodsCatalogId;
		this.parentCatalogId = parentCatalogId;
		this.goodsCatalogName = goodsCatalogName;
		this.goodsCatalogStatus = goodsCatalogStatus;
	}
	public GoodsCatalog() {
		super();
	}
	
	
}
